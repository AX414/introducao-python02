# Como fazer com que a frase inteira fique em
# maiusculo/minusculo/primeira letra em maiusculo?
frase = "pequena frase"
frase = frase.upper()
print(f"Tudo Maiusculo: {frase}")

frase = frase.lower()
print(f"Tudo Minusculo: {frase}")

frase = frase.capitalize()
print(f"Primeira Maiuscula: {frase}")